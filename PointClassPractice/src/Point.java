public class Point {
	//Declares Instance Variables
	private double x;
	private double y;
	
	//Default Constructor
	public Point() {
		x = 0.0;
		y = 0.0;
	}
	
	//Constructor with points
	public Point(double newX, double newY) {
		x = newX;
		y = newY;
	}
}
